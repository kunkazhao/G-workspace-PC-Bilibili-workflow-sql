def build_price_transition_title_image(
    title: str,
    output_dir: Path,
    canvas_width: int,
    canvas_height: int,
) -> Path:
    output_dir.mkdir(parents=True, exist_ok=True)
    title_hash = hashlib.sha1(title.encode("utf-8")).hexdigest()[:10]
    output_path = output_dir / f"{safe_path_component(title, 'price-title')}-{title_hash}.png"
    if output_path.exists():
        return output_path

    image = Image.new("RGBA", (canvas_width, canvas_height), (0, 0, 0, 0))
    draw = ImageDraw.Draw(image)
    font_size = max(96, min(170, int(canvas_height * 0.14)))
    max_width = canvas_width * 0.82
    while font_size > 72:
        font = load_price_title_font(font_size)
        bbox = draw.textbbox((0, 0), title, font=font)
        if bbox[2] - bbox[0] <= max_width:
            break
        font_size -= 6

    x = canvas_width / 2
    y = canvas_height / 2
    for offset, alpha in ((8, 95), (4, 130)):
        draw.text((x + offset, y + offset), title, font=font, anchor="mm", fill=(0, 0, 0, alpha))
    draw.text((x, y), title, font=font, anchor="mm", fill=(255, 255, 255, 255))
    image.save(output_path)
    return output_path


