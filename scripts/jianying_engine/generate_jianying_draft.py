def build_media_clip_settings(media_path: Path, canvas_width: int, canvas_height: int) -> Any | None:
    if media_path.suffix.lower() not in IMAGE_SUFFIXES:
        return None

    try:
        with Image.open(media_path) as image:
            source_width, source_height = image.size
    except Exception:
        return None

    if source_width <= 0 or source_height <= 0 or canvas_width <= 0 or canvas_height <= 0:
        return None

    fit_scale = min(canvas_width / source_width, canvas_height / source_height)
    display_height = source_height * fit_scale
    vertical_blank = max(canvas_height - display_height, 0.0)
    if vertical_blank <= 1.0:
        return None

    transform_y = vertical_blank / canvas_height
    return draft.ClipSettings(transform_y=transform_y)


def build_display_video_clip_settings(
    display_video_path: Path,
    slot: dict[str, Any],
    canvas_width: int,
    canvas_height: int,
) -> Any:
    source_width, source_height = probe_video_size(display_video_path)
    if source_width <=0 or source_height <=0:
        return draft.ClipSettings()
    slot_x = float(slot.get('x') or 0)
    slot_y = float(slot.get('y') or 0)
    slot_width = float(slot.get('width') or 0)
    slot_height = float(slot.get('height') or 0)
    if slot_width <=0 or slot_height <=0 or canvas_width <=0 or canvas_height <=0:
        return draft.ClipSettings()

    #剪映 clip_settings.scale 是相对 source size 的缩放系数，
    # clip_settings.transform 是相对画布中心的位移（单位：半画布宽/高）。
    #视频实际渲染尺寸 = source * scale，video中心 = canvas_center + transform * canvas/2。
    center_x = slot_x + slot_width /2
    center_y = slot_y + slot_height /2
    transform_x = (center_x - canvas_width /2) / (canvas_width /2)
    # pyJianYingDraft/剪映的 Y 轴位移和常规屏幕坐标方向相反，向下需要负值。
    transform_y = -((center_y - canvas_height /2) / (canvas_height /2))

    if normalize_text(slot.get('coordinate_mode')) == 'clip_transform_pixels':
        # clip_transform_pixels模式：视频直接贴到 slot矩形（允许变形），渲染尺寸 = slot尺寸。
        scale_x = slot_width / source_width
        scale_y = slot_height / source_height
        return draft.ClipSettings(
            scale_x=scale_x,
            scale_y=scale_y,
            transform_x=transform_x,
            transform_y=transform_y,
        )

    # 默认模式：保持视频宽高比，让视频填满 slot 长边，video中心 = slot中心（letterbox/pillarbox）。
    source_aspect = source_width / source_height
    slot_aspect = slot_width / slot_height
    if source_aspect >= slot_aspect:
        scale = slot_width / source_width
    else:
        scale = slot_height / source_height
    override_scale = slot.get('display_scale')
    if override_scale is not None:
        try:
            scale = float(override_scale)
        except (TypeError, ValueError):
            pass
    return draft.ClipSettings(
        scale_x=scale,
        scale_y=scale,
        transform_x=transform_x,
        transform_y=transform_y,
    )

