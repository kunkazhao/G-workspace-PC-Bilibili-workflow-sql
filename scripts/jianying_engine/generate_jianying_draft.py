def load_manifest_payload(path: Path) -> tuple[list[dict[str, Any]], str]:
    payload = json.loads(path.read_text(encoding="utf-8-sig"))
    if isinstance(payload, list):
        return payload, "legacy_items"
    if isinstance(payload, dict):
        if isinstance(payload.get("items"), list):
            return payload["items"], "legacy_items"
        if isinstance(payload.get("entries"), list):
            entries = sorted(
                [entry for entry in payload["entries"] if isinstance(entry, dict)],
                key=lambda entry: int(entry.get("order_index") or 0),
            )
            return entries, "b_workflow_entries"
    raise SkillError("Manifest 必须是列表，或包含 `items` / `entries` 的对象。")


def fallback_manifest_media_paths(
    payload: Any,
    *,
    image_category: str,
    image_user: str,
    image_set: str,
    image_index_path: Path,
) -> dict[str, Any]:
    if not isinstance(payload, dict):
        return {
            "image_category": image_category,
            "image_user": image_user,
            "image_set": image_set,
            "resolved_count": 0,
            "missing_uids": [],
        }

    entries = payload.get("entries")
    if not isinstance(entries, list):
        return {
            "image_category": image_category,
            "image_user": image_user,
            "image_set": image_set,
            "resolved_count": 0,
            "missing_uids": [],
        }

    effective_category = normalize_text(image_category or payload.get("image_category"))
    effective_user = normalize_text(image_user or payload.get("image_user"))
    effective_set = normalize_text(image_set or payload.get("image_set"))
    if not all((effective_category, effective_user, effective_set)):
        return {
            "image_category": effective_category,
            "image_user": effective_user,
            "image_set": effective_set,
            "resolved_count": 0,
            "missing_uids": [],
        }

    needs_fallback: list[str] = []
    seen_uids: set[str] = set()
    for raw in entries:
        if not isinstance(raw, dict):
            continue
        if normalize_text(raw.get("type")) != "product":
            continue
        if normalize_text(raw.get("image_path")) or normalize_text(raw.get("video_path")):
            continue
        uid = normalize_text(raw.get("product_uid"))
        if uid and uid not in seen_uids:
            needs_fallback.append(uid)
            seen_uids.add(uid)

    if not needs_fallback:
        return {
            "image_category": effective_category,
            "image_user": effective_user,
            "image_set": effective_set,
            "resolved_count": 0,
            "missing_uids": [],
        }

    indexed_image_paths, missing_uids = resolve_image_paths(
        index_path=image_index_path,
        category=effective_category,
        image_user=effective_user,
        image_set=effective_set,
        product_uids=needs_fallback,
    )

    resolved_count = 0
    for raw in entries:
        if not isinstance(raw, dict):
            continue
        if normalize_text(raw.get("type")) != "product":
            continue
        if normalize_text(raw.get("image_path")) or normalize_text(raw.get("video_path")):
            continue
        uid = normalize_text(raw.get("product_uid"))
        resolved_path = indexed_image_paths.get(uid, "")
        if resolved_path:
            raw["image_path"] = resolved_path
            resolved_count += 1

    payload["image_category"] = effective_category
    payload["image_user"] = effective_user
    payload["image_set"] = effective_set
    payload["image_index_path"] = str(image_index_path)
    return {
        "image_category": effective_category,
        "image_user": effective_user,
        "image_set": effective_set,
        "resolved_count": resolved_count,
        "missing_uids": missing_uids,
    }


def probe_audio_duration(audio_path: Path) -> float:
    try:
        return draft.AudioMaterial(str(audio_path)).duration / 1_000_000
    except Exception:
        pass

    command = [
        "ffprobe",
        "-v",
        "error",
        "-show_entries",
        "format=duration",
        "-of",
        "json",
        str(audio_path),
    ]
    result = subprocess.run(command, capture_output=True, text=True, encoding="utf-8", errors="ignore", check=True)
    payload = json.loads(result.stdout)
    duration_text = payload.get("format", {}).get("duration")
    if not duration_text:
        raise SkillError(f"无法读取音频时长：{audio_path}")
    duration = float(duration_text)
    if duration <= 0:
        raise SkillError(f"音频时长必须大于 0：{audio_path}")
    return duration


