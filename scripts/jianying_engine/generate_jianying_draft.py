def partition_clauses_to_segments(clauses: list[str], segment_weights: list[float]) -> list[str]:
    if not clauses or not segment_weights:
        return []

    while len(clauses) < len(segment_weights):
        previous = list(clauses)
        clauses = split_longest_clause(clauses)
        if clauses == previous:
            break

    if len(clauses) <= len(segment_weights):
        return clauses + [""] * (len(segment_weights) - len(clauses))

    clause_lengths = [max(len(clause), 1) for clause in clauses]
    total_clause_length = sum(clause_lengths)
    weight_sum = sum(segment_weights) or float(len(segment_weights))
    targets = [weight / weight_sum * total_clause_length for weight in segment_weights]

    prefix = [0]
    for length in clause_lengths:
        prefix.append(prefix[-1] + length)

    segment_count = len(segment_weights)
    clause_count = len(clauses)
    dp = [[math.inf] * (clause_count + 1) for _ in range(segment_count + 1)]
    prev = [[-1] * (clause_count + 1) for _ in range(segment_count + 1)]
    dp[0][0] = 0.0

    for segment_index in range(1, segment_count + 1):
        min_clause_index = segment_index
        max_clause_index = clause_count - (segment_count - segment_index)
        for clause_index in range(min_clause_index, max_clause_index + 1):
            for start_index in range(segment_index - 1, clause_index):
                group_length = prefix[clause_index] - prefix[start_index]
                cost = (group_length - targets[segment_index - 1]) ** 2
                candidate = dp[segment_index - 1][start_index] + cost
                if candidate < dp[segment_index][clause_index]:
                    dp[segment_index][clause_index] = candidate
                    prev[segment_index][clause_index] = start_index

    groups: list[str] = []
    clause_index = clause_count
    for segment_index in range(segment_count, 0, -1):
        start_index = prev[segment_index][clause_index]
        if start_index < 0:
            start_index = max(0, clause_index - 1)
        groups.append("".join(clauses[start_index:clause_index]))
        clause_index = start_index
    groups.reverse()
    return groups


def build_subtitle_segments(
    audio_path: Path,
    transcript_text: str,
    offset_sec: float,
    model_name: str,
    language: str,
    engine: str = "whisper",
    *,
    vad_filter: bool = True,
) -> list[SubtitleSegment]:
    clauses = split_transcript_clauses(transcript_text)
    if not clauses:
        return []
    if engine == "qwen":
        return build_qwen_subtitle_segments(audio_path, clauses, offset_sec, language)

    asr_segments = run_alignment_asr(audio_path, model_name, language, vad_filter=vad_filter)
    if not asr_segments:
        return []

    weights = [
        max(len(normalize_subtitle_text(segment.get("text", ""))), segment["end"] - segment["start"], 1.0)
        for segment in asr_segments
    ]
    texts = partition_clauses_to_segments(clauses, weights)

    results: list[SubtitleSegment] = []
    for segment, text in zip(asr_segments, texts):
        display_text = text.strip()
        if not normalize_subtitle_text(display_text):
            continue
        results.append(
            SubtitleSegment(
                start_sec=offset_sec + float(segment["start"]),
                duration_sec=max(float(segment["end"]) - float(segment["start"]), 0.1),
                text=display_text,
            )
        )
    return results


def get_qwen_forced_aligner() -> Any:
    if QWEN_FORCED_ALIGNER_MODEL in QWEN_ALIGNER_CACHE:
        return QWEN_ALIGNER_CACHE[QWEN_FORCED_ALIGNER_MODEL]
    if Qwen3ForcedAligner is None or torch is None:
        raise SkillError("当前环境未安装 qwen-asr 或 torch，无法使用 Qwen 强制对齐。")

    model_kwargs: dict[str, Any] = {}
    if torch.cuda.is_available():
        model_kwargs["device_map"] = "cuda:0"
        model_kwargs["dtype"] = torch.bfloat16
    else:
        model_kwargs["device_map"] = "cpu"
    aligner = Qwen3ForcedAligner.from_pretrained(QWEN_FORCED_ALIGNER_MODEL, **model_kwargs)
    QWEN_ALIGNER_CACHE[QWEN_FORCED_ALIGNER_MODEL] = aligner
    return aligner


def build_qwen_subtitle_segments(
    audio_path: Path,
    clauses: list[str],
    offset_sec: float,
    language: str,
) -> list[SubtitleSegment]:
    aligner = get_qwen_forced_aligner()
    qwen_language = QWEN_LANGUAGE_MAP.get(language.lower(), language)
    results = aligner.align(audio=str(audio_path), text="".join(clauses), language=qwen_language)
    items = results[0].items if isinstance(results, list) else results.items

    clause_segments: list[SubtitleSegment] = []
    item_index = 0
    for clause in clauses:
        target = clause
        built = ""
        start_time: float | None = None
        end_time: float | None = None
        while item_index < len(items) and len(built) < len(target):
            token_text = normalize_subtitle_text(getattr(items[item_index], "text", ""))
            if token_text:
                if start_time is None:
                    start_time = float(getattr(items[item_index], "start_time", 0.0))
                end_time = float(getattr(items[item_index], "end_time", 0.0))
                built += token_text
            item_index += 1

        if not built or start_time is None or end_time is None:
            continue
        clause_segments.append(
            SubtitleSegment(
                start_sec=offset_sec + start_time,
                duration_sec=max(end_time - start_time, 0.1),
                text=target,
            )
        )
    return clause_segments


