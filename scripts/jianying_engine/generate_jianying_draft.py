def load_template_draft(template_dir: Path) -> tuple[dict[str, Any], dict[str, Any]]:
    if not template_dir.exists():
        raise FileNotFoundError(f"模板草稿不存在：{template_dir}")
    if not template_dir.is_dir():
        raise NotADirectoryError(f"模板草稿必须是目录：{template_dir}")

    draft_content_path = template_dir / "draft_content.json"
    draft_meta_path = template_dir / "draft_meta_info.json"
    subdraft_content_path = template_dir / "subdraft" / "draft_content.json"

    content: dict[str, Any] | None = None
    content_error = ""
    for candidate_path, source_label in (
        (draft_content_path, "draft_content.json"),
        (subdraft_content_path, "subdraft/draft_content.json"),
    ):
        if not candidate_path.exists():
            continue
        try:
            loaded = json.loads(candidate_path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError, UnicodeDecodeError) as exc:
            content_error = f"{source_label} 不可解析：{exc}"
            continue
        if isinstance(loaded, dict):
            content = loaded
            break
        content_error = f"{source_label} 结构异常，必须是对象"

    if content is None:
        if not draft_content_path.exists() and not subdraft_content_path.exists():
            raise SkillError(
                f"模板草稿目录缺少可用内容文件：{template_dir}。"
                " 需要 draft_content.json，或 subdraft/draft_content.json。"
            )
        raise SkillError(
            "模板草稿里没有可读取的明文时间线 JSON。"
            f" 当前目录不能直接当模板使用：{template_dir}。"
            f" 详情：{content_error or '未找到可解析内容'}"
        )

    meta: dict[str, Any] = {}
    if draft_meta_path.exists():
        try:
            loaded_meta = json.loads(draft_meta_path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError, UnicodeDecodeError):
            loaded_meta = {}
        if isinstance(loaded_meta, dict):
            meta = loaded_meta
    return content, meta


def collect_ids(payload: Any, sink: set[str]) -> None:
    if isinstance(payload, dict):
        value = payload.get("id")
        if isinstance(value, str) and value:
            sink.add(value)
        for child in payload.values():
            collect_ids(child, sink)
        return
    if isinstance(payload, list):
        for child in payload:
            collect_ids(child, sink)


def remap_known_ids(payload: Any, id_map: dict[str, str]) -> Any:
    if isinstance(payload, dict):
        return {key: remap_known_ids(value, id_map) for key, value in payload.items()}
    if isinstance(payload, list):
        return [remap_known_ids(value, id_map) for value in payload]
    if isinstance(payload, str):
        return id_map.get(payload, payload)
    return payload


def shift_track_timeranges(tracks: list[dict[str, Any]], offset_us: int) -> None:
    for track in tracks:
        for segment in track.get("segments", []):
            timerange = segment.get("target_timerange")
            if isinstance(timerange, dict):
                timerange["start"] = int(timerange.get("start", 0)) + offset_us


def ensure_list_field(container: dict[str, Any], key: str) -> list[Any]:
    value = container.get(key)
    if isinstance(value, list):
        return value
    container[key] = []
    return container[key]


def as_list(value: Any) -> list[Any]:
    return value if isinstance(value, list) else []


def resolve_draft_duration_us(content: dict[str, Any]) -> int:
    raw_duration = int(content.get("duration") or 0)
    if raw_duration > 0:
        return raw_duration

    max_end = 0
    for track in as_list(content.get("tracks")):
        if not isinstance(track, dict):
            continue
        for segment in as_list(track.get("segments")):
            if not isinstance(segment, dict):
                continue
            timerange = segment.get("target_timerange")
            if not isinstance(timerange, dict):
                continue
            start = int(timerange.get("start") or 0)
            duration = int(timerange.get("duration") or 0)
            max_end = max(max_end, start + duration)
    return max_end


def resolve_primary_timeline_id(draft_dir: Path) -> tuple[str, str]:
    timeline_root = draft_dir / "Timelines"
    project_path = timeline_root / "project.json"
    layout_path = draft_dir / "timeline_layout.json"
    project_payload = read_json_object(project_path)
    layout_payload = read_json_object(layout_path)

    candidate_ids: list[str] = []
    for value in (
        project_payload.get("main_timeline_id"),
        layout_payload.get("activeTimeline"),
    ):
        if isinstance(value, str) and value.strip():
            candidate_ids.append(value.strip())

    timelines = project_payload.get("timelines")
    if isinstance(timelines, list):
        for item in timelines:
            if not isinstance(item, dict):
                continue
            value = item.get("id")
            if isinstance(value, str) and value.strip():
                candidate_ids.append(value.strip())

    for timeline_id in candidate_ids:
        timeline_dir = timeline_root / timeline_id
        if timeline_dir.exists():
            return timeline_id, timeline_id

    if candidate_ids:
        return candidate_ids[0], candidate_ids[0]
    generated_id = uuid.uuid4().hex
    return generated_id, generated_id


def sync_modern_timeline_files(draft_dir: Path, draft_name: str, draft_content: dict[str, Any]) -> None:
    timeline_root = draft_dir / "Timelines"
    project_path = timeline_root / "project.json"
    layout_path = draft_dir / "timeline_layout.json"
    timeline_id, timeline_name = resolve_primary_timeline_id(draft_dir)

    timeline_dir = timeline_root / timeline_id
    timeline_dir.mkdir(parents=True, exist_ok=True)
    write_json_object(timeline_dir / "draft_content.json", draft_content)

    project_payload = read_json_object(project_path)
    project_timelines = project_payload.get("timelines")
    if not isinstance(project_timelines, list):
        project_timelines = []

    timeline_entry = None
    for item in project_timelines:
        if isinstance(item, dict) and item.get("id") == timeline_id:
            timeline_entry = item
            break
    if timeline_entry is None:
        timeline_entry = {}
        project_timelines.insert(0, timeline_entry)

    timeline_entry["id"] = timeline_id
    timeline_entry["is_marked_delete"] = False
    timeline_entry["name"] = normalize_text(timeline_entry.get("name") or draft_name or timeline_name or timeline_id)
    timeline_entry["create_time"] = int(timeline_entry.get("create_time") or 0)
    timeline_entry["update_time"] = int(timeline_entry.get("update_time") or 0)

    project_payload["main_timeline_id"] = timeline_id
    project_payload["timelines"] = project_timelines
    write_json_object(project_path, project_payload)

    layout_payload = read_json_object(layout_path)
    layout_payload["activeTimeline"] = timeline_id
    dock_items = layout_payload.get("dockItems")
    if not isinstance(dock_items, list) or not dock_items:
        dock_items = [{"dockIndex": 0, "ratio": 1, "timelineIds": [], "timelineNames": []}]
        layout_payload["dockItems"] = dock_items
    primary_dock = dock_items[0]
    if not isinstance(primary_dock, dict):
        primary_dock = {"dockIndex": 0, "ratio": 1, "timelineIds": [], "timelineNames": []}
        dock_items[0] = primary_dock
    primary_dock["timelineIds"] = [timeline_id]
    primary_dock["timelineNames"] = [timeline_entry["name"]]
    write_json_object(layout_path, layout_payload)


def ensure_output_draft_dir(template_dir: Path, output_dir: Path, allow_replace: bool) -> None:
    if template_dir.resolve() == output_dir.resolve():
        raise SkillError("输出草稿目录不能和模板草稿目录相同，避免污染模板源。")
    if output_dir.exists() and not allow_replace:
        raise SkillError(f"目标草稿已存在，请换一个草稿名或启用覆盖：{output_dir}")
    output_dir.parent.mkdir(parents=True, exist_ok=True)
    shutil.copytree(template_dir, output_dir, dirs_exist_ok=True)


