def normalize_text(value: Any) -> str:
    return str(value or "").strip()


def price_transition_title_from_entry(raw: dict[str, Any]) -> str:
    section = normalize_text(raw.get("section"))
    product_uid = normalize_text(raw.get("product_uid")).casefold()
    source_label = normalize_text(raw.get("source_label"))
    if section != "price_transition" and product_uid != "price_transition" and not source_label.startswith("价格过渡"):
        return ""

    title = normalize_text(raw.get("price_range_label"))
    if not title:
        title = source_label
        title = re.sub(r"^价格过渡\s*[:：-]?\s*", "", title).strip()
    return title


def safe_path_component(value: str, fallback: str = "draft") -> str:
    text = re.sub(r'[<>:"/\\|?*\x00-\x1f]+', "_", normalize_text(value))
    text = text.strip(" ._")
    return text or fallback


