def media_meta_entry(material: dict[str, Any], *, outer_type: int, metetype: str, now_us: int) -> dict[str, Any] | None:
    path_text = normalize_text(material.get("path"))
    if not path_text:
        return None
    path = Path(path_text)
    if not path.exists():
        return None
    try:
        stat = path.stat()
    except OSError:
        return None
    name = normalize_text(material.get("material_name") or material.get("name") or path.name)
    item_id = normalize_text(material.get("local_material_id") or material.get("id") or material.get("material_id"))
    return {
        "create_time": int(stat.st_ctime),
        "duration": int(material.get("duration") or 0),
        "extra_info": name,
        "file_Path": path_text,
        "height": int(material.get("height") or 0),
        "id": item_id or uuid.uuid4().hex,
        "import_time": int(now_us / 1_000_000),
        "import_time_ms": now_us,
        "item_source": 1,
        "md5": "",
        "metetype": metetype,
        "roughcut_time_range": {"duration": -1, "start": -1},
        "sub_time_range": {"duration": -1, "start": -1},
        "type": outer_type,
        "width": int(material.get("width") or 0),
    }


def build_meta_draft_materials(content_payload: dict[str, Any]) -> list[dict[str, Any]]:
    draft_materials = empty_meta_draft_materials()
    by_type = {int(item["type"]): item["value"] for item in draft_materials}
    materials = content_payload.get("materials")
    if not isinstance(materials, dict):
        return draft_materials
    now_us = int(time.time() * 1_000_000)
    seen_paths: set[tuple[int, str]] = set()

    videos = materials.get("videos")
    if isinstance(videos, list):
        for material in videos:
            if not isinstance(material, dict):
                continue
            metetype = normalize_text(material.get("type")) or "video"
            entry = media_meta_entry(material, outer_type=0, metetype=metetype, now_us=now_us)
            if entry is None:
                continue
            key = (0, entry["file_Path"].casefold())
            if key in seen_paths:
                continue
            seen_paths.add(key)
            by_type[0].append(entry)

    audios = materials.get("audios")
    if isinstance(audios, list):
        for material in audios:
            if not isinstance(material, dict):
                continue
            entry = media_meta_entry(material, outer_type=8, metetype="none", now_us=now_us)
            if entry is None:
                continue
            key = (8, entry["file_Path"].casefold())
            if key in seen_paths:
                continue
            seen_paths.add(key)
            by_type[8].append(entry)
    return draft_materials


def read_json_object(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    try:
        loaded = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError, UnicodeDecodeError):
        return {}
    return loaded if isinstance(loaded, dict) else {}


def write_json_object(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


