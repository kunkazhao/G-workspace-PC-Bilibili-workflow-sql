def primary_draft_content_path(draft_dir: Path) -> Path | None:
    for candidate in (
        draft_dir / "draft_content.json",
        draft_dir / "template-2.tmp",
        draft_dir / "template.tmp",
    ):
        if candidate.exists() and candidate.stat().st_size > 0:
            return candidate
    timeline_root = draft_dir / "Timelines"
    if timeline_root.exists():
        for candidate in timeline_root.rglob("draft_content.json"):
            if candidate.exists() and candidate.stat().st_size > 0:
                return candidate
    return None


def draft_content_paths(draft_dir: Path) -> list[Path]:
    paths: list[Path] = []
    for candidate in (
        draft_dir / "draft_content.json",
        draft_dir / "draft_content.json.bak",
        draft_dir / "template-2.tmp",
        draft_dir / "template.tmp",
    ):
        if candidate.exists() and candidate.stat().st_size > 0:
            paths.append(candidate)
    timeline_root = draft_dir / "Timelines"
    if timeline_root.exists():
        paths.extend(
            candidate
            for candidate in timeline_root.rglob("draft_content.json")
            if candidate.exists() and candidate.stat().st_size > 0
        )
    seen: set[Path] = set()
    unique: list[Path] = []
    for path in paths:
        resolved = path.resolve()
        if resolved not in seen:
            seen.add(resolved)
            unique.append(path)
    return unique


def normalize_content_media_materials(payload: dict[str, Any]) -> bool:
    changed = False
    materials = payload.get("materials")
    if not isinstance(materials, dict):
        return False
    videos = materials.get("videos")
    if not isinstance(videos, list):
        return False
    for material in videos:
        if not isinstance(material, dict):
            continue
        material_id = normalize_text(material.get("id") or material.get("material_id"))
        if not material_id:
            continue
        if not normalize_text(material.get("local_material_id")):
            material["local_material_id"] = material_id
            changed = True
        if not normalize_text(material.get("material_id")):
            material["material_id"] = material_id
            changed = True
    return changed


