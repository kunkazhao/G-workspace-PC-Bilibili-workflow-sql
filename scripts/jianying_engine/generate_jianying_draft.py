def is_openable_draft_dir(draft_dir: Path) -> bool:
    if not draft_dir.exists() or not draft_dir.is_dir():
        return False
    for candidate in (
        draft_dir / "draft_content.json",
        draft_dir / "template-2.tmp",
        draft_dir / "template.tmp",
    ):
        if candidate.exists() and candidate.stat().st_size > 0:
            return True
    timeline_root = draft_dir / "Timelines"
    return timeline_root.exists() and any(path.stat().st_size > 0 for path in timeline_root.rglob("draft_content.json"))


def replace_draft_dir_atomically(staging_dir: Path, final_dir: Path, allow_replace: bool) -> None:
    if not is_openable_draft_dir(staging_dir):
        raise SkillError(f"生成结果缺少草稿内容文件，已停止替换正式草稿：{staging_dir}")
    if final_dir.exists():
        if not allow_replace:
            raise SkillError(f"目标草稿已存在，请换一个草稿名或启用覆盖：{final_dir}")
        backup_dir = final_dir.with_name(f"{final_dir.name}.__replace_backup__")
        if backup_dir.exists():
            shutil.rmtree(backup_dir)
        final_dir.rename(backup_dir)
        try:
            staging_dir.rename(final_dir)
            shutil.rmtree(backup_dir, ignore_errors=True)
        except Exception:
            if final_dir.exists():
                shutil.rmtree(final_dir, ignore_errors=True)
            if backup_dir.exists():
                backup_dir.rename(final_dir)
            raise
        return
    staging_dir.rename(final_dir)


def copy_template_assets_into_shell(template_dir: Path, output_dir: Path) -> None:
    excluded_names = {
        ".backup",
        "draft_meta_info.json",
        "draft_content.json",
        "draft_content.json.bak",
        "template-2.tmp",
        "timeline_layout.json",
        "Timelines",
        "draft_biz_config.json",
    }
    for child in template_dir.iterdir():
        if child.name in excluded_names:
            continue
        target = output_dir / child.name
        if child.is_dir():
            shutil.copytree(child, target, dirs_exist_ok=True)
        elif child.is_file():
            target.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(child, target)


def write_draft_content_payload(base_file: Path, payload: dict[str, Any]) -> None:
    write_json_object(base_file, payload)
    for sibling_name in ("draft_content.json.bak", "template-2.tmp", "template.tmp"):
        sibling_path = base_file.with_name(sibling_name)
        if sibling_path.exists():
            write_json_object(sibling_path, payload)


def write_into_active_timeline_if_present(draft_dir: Path, payload: dict[str, Any]) -> None:
    layout_path = draft_dir / "timeline_layout.json"
    project_path = draft_dir / "Timelines" / "project.json"
    layout_payload = read_json_object(layout_path)
    project_payload = read_json_object(project_path)
    candidate_ids: list[str] = []
    active_id = normalize_text(layout_payload.get("activeTimeline"))
    if active_id:
        candidate_ids.append(active_id)
    main_id = normalize_text(project_payload.get("main_timeline_id"))
    if main_id and main_id not in candidate_ids:
        candidate_ids.append(main_id)
    for timeline in as_list(project_payload.get("timelines")):
        if isinstance(timeline, dict):
            timeline_id = normalize_text(timeline.get("id"))
            if timeline_id and timeline_id not in candidate_ids:
                candidate_ids.append(timeline_id)

    for timeline_id in candidate_ids:
        timeline_file = draft_dir / "Timelines" / timeline_id / "draft_content.json"
        if timeline_file.exists():
            write_draft_content_payload(timeline_file, payload)
            return


def merge_template_with_generated_tail(
    template_dir: Path,
    generated_tail_dir: Path,
    output_dir: Path,
    output_name: str,
    allow_replace: bool,
    *,
    draft_root: Path,
    canvas_width: int,
    canvas_height: int,
    fps: int,
) -> tuple[float, dict[str, int]]:
    template_content, _template_meta = load_template_draft(template_dir)
    tail_content, _tail_meta = load_template_draft(generated_tail_dir)
    output_dir = create_shell_draft(
        draft_root=draft_root,
        draft_name=output_name,
        canvas_width=canvas_width,
        canvas_height=canvas_height,
        fps=fps,
        allow_replace=allow_replace,
    )
    shell_meta = read_json_object(output_dir / "draft_meta_info.json")
    copy_template_assets_into_shell(template_dir, output_dir)

    merged_content = copy.deepcopy(template_content)
    template_duration_us = resolve_draft_duration_us(merged_content)
    tail_duration_us = resolve_draft_duration_us(tail_content)

    tail_ids: set[str] = set()
    collect_ids(tail_content, tail_ids)
    id_map = {old_id: uuid.uuid4().hex for old_id in tail_ids}
    remapped_tail = remap_known_ids(copy.deepcopy(tail_content), id_map)
    shift_track_timeranges(remapped_tail.get("tracks", []), template_duration_us)

    merged_content["id"] = uuid.uuid4().hex
    merged_content["name"] = output_name
    merged_content["duration"] = template_duration_us + tail_duration_us
    merged_content["update_time"] = 0
    merged_content["create_time"] = 0

    merged_materials = merged_content.setdefault("materials", {})
    for key, tail_items in (remapped_tail.get("materials") or {}).items():
        if isinstance(tail_items, list):
            ensure_list_field(merged_materials, key).extend(tail_items)

    merged_keyframes = merged_content.setdefault("keyframes", {})
    for key, tail_items in (remapped_tail.get("keyframes") or {}).items():
        if isinstance(tail_items, list):
            ensure_list_field(merged_keyframes, key).extend(tail_items)

    ensure_list_field(merged_content, "relationships").extend(as_list(remapped_tail.get("relationships")))
    ensure_list_field(merged_content, "keyframe_graph_list").extend(as_list(remapped_tail.get("keyframe_graph_list")))
    ensure_list_field(merged_content, "time_marks").extend(as_list(remapped_tail.get("time_marks")))
    ensure_list_field(merged_content, "tracks").extend(as_list(remapped_tail.get("tracks")))

    write_draft_content_payload(output_dir / "draft_content.json", merged_content)
    write_into_active_timeline_if_present(output_dir, merged_content)
    return merged_content["duration"] / 1_000_000, {
        "template_duration_us": template_duration_us,
        "tail_duration_us": tail_duration_us,
        "appended_tracks": len(remapped_tail.get("tracks", [])),
    }


def build_generated_draft(
    *,
    draft_root: Path,
    draft_name: str,
    canvas_width: int,
    canvas_height: int,
    fps: int,
    background_image: Path,
    items: list[ClipItem],
    intro_item: ClipItem | None,
    manifest_path: Path | None,
    manifest_mode: str,
    match_summary: dict[str, Any],
    subtitle_model: str,
    subtitle_language: str,
    subtitle_engine: str,
    skip_subtitles: bool,
    subtitle_vad_filter: bool,
    allow_replace: bool,
) -> dict[str, Any]:
    timeline_items = ([intro_item] if intro_item is not None else []) + items
    voice_gap_count = max(len(timeline_items) - 1, 0)
    total_voice_gap_sec = voice_gap_count * VOICE_CLIP_GAP_SEC
    total_duration_sec = sum(item.duration_sec for item in timeline_items) + total_voice_gap_sec
    draft_folder = draft.DraftFolder(str(draft_root))
    script = draft_folder.create_draft(
        draft_name,
        canvas_width,
        canvas_height,
        fps=fps,
        allow_replace=allow_replace,
    )
    script.add_track(draft.TrackType.video, "background")
    script.add_track(draft.TrackType.video, "media", relative_index=1)
    script.add_track(draft.TrackType.video, "display_video", relative_index=2)
    script.add_track(draft.TrackType.audio, "voice")

    subtitle_style = draft.TextStyle(size=5, align=1, auto_wrapping=True)
    subtitle_clip = draft.ClipSettings(transform_y=-0.8)
    subtitle_segments: list[SubtitleSegment] = []
    missing_subtitle_texts: list[str] = []

    background_segment = draft.VideoSegment(
        str(background_image),
        trange("0s", sec_text(total_duration_sec)),
    )
    script.add_segment(background_segment, "background")
    price_title_image_dir = draft_root / PRICE_TITLE_CACHE_DIR_NAME / safe_path_component(draft_name)

    audio_cursor = 0.0
    visual_cursor = 0.0
    media_segments: list[tuple[int, Any]] = []
    display_video_segments: list[Any] = []
    audio_segments: list[Any] = []
    price_transition_title_segments: list[Any] = []
    for item_index, item in enumerate(timeline_items):
        if not skip_subtitles and item.source_type != "intro_video":
            if item.subtitle_text:
                subtitle_segments.extend(
                    build_subtitle_segments(
                        item.audio_path,
                        item.subtitle_text,
                        audio_cursor,
                        subtitle_model,
                        subtitle_language,
                        subtitle_engine,
                        vad_filter=subtitle_vad_filter,
                    )
                )
            else:
                missing_subtitle_texts.append(item.audio_path.name)

        if item.media_path is not None:
            media_duration_sec = item.duration_sec
            media_segments.append(
                (
                    item_index,
                    draft.VideoSegment(
                        str(item.media_path),
                        trange(sec_text(visual_cursor), sec_text(media_duration_sec)),
                        clip_settings=build_media_clip_settings(item.media_path, canvas_width, canvas_height),
                    ),
                )
            )
        if item.display_video_path is not None and item.display_video_slot is not None:
            display_duration = max(min(probe_media_duration(item.display_video_path), item.duration_sec) - 0.02, 0.1)
            display_video_segments.append(
                draft.VideoSegment(
                    str(item.display_video_path),
                    trange(sec_text(visual_cursor), sec_text(display_duration)),
                    source_timerange=trange("0s", sec_text(display_duration)),
                    volume=0.0,
                    clip_settings=build_display_video_clip_settings(
                        item.display_video_path,
                        item.display_video_slot,
                        canvas_width,
                        canvas_height,
                    ),
                )
            )
        if item.source_type != "intro_video":
            if item.price_transition_title:
                price_transition_title_segments.append(
                    build_price_transition_title_segment(
                        item.price_transition_title,
                        audio_cursor,
                        item.duration_sec,
                        price_title_image_dir,
                        canvas_width,
                        canvas_height,
                    )
                )
            audio_segments.append(
                draft.AudioSegment(
                    str(item.audio_path),
                    trange(sec_text(audio_cursor), sec_text(item.duration_sec)),
                )
            )
        visual_cursor += item.duration_sec
        audio_cursor += item.duration_sec
        if item_index < len(timeline_items) - 1:
            audio_cursor += VOICE_CLIP_GAP_SEC

    transition_type = getattr(draft.TransitionType, "翻页", None)
    if transition_type is not None:
        for index in range(len(media_segments) - 1):
            current_item_index, media_segment = media_segments[index]
            next_item_index, _next_media_segment = media_segments[index + 1]
            if next_item_index != current_item_index + 1:
                continue
            if timeline_items[current_item_index].source_type == "intro_video":
                continue
            media_segment.add_transition(transition_type)

    for _item_index, media_segment in media_segments:
        script.add_segment(media_segment, "media")
    for display_video_segment in display_video_segments:
        script.add_segment(display_video_segment, "display_video")
    for audio_segment in audio_segments:
        script.add_segment(audio_segment, "voice")

    if price_transition_title_segments:
        script.add_track(draft.TrackType.video, "price_transition_title", relative_index=3)
        for title_segment in price_transition_title_segments:
            script.add_segment(title_segment, "price_transition_title")

    if subtitle_segments:
        script.add_track(draft.TrackType.text, "subtitle", relative_index=999)
        for subtitle in subtitle_segments:
            script.add_segment(
                draft.TextSegment(
                    subtitle.text,
                    trange(sec_text(subtitle.start_sec), sec_text(subtitle.duration_sec)),
                    style=subtitle_style,
                    clip_settings=subtitle_clip,
                ),
                "subtitle",
            )

    script.save()
    draft_dir = draft_root / draft_name
    return {
        "draft_root": str(draft_root),
        "draft_name": draft_name,
        "draft_dir": str(draft_dir),
        "manifest_path": str(manifest_path) if manifest_path else "",
        "match_mode": manifest_mode,
        "subtitle_engine": subtitle_engine,
        "total_items": len(timeline_items),
        "product_items": len(items),
        "total_duration_sec": round(audio_cursor, 3),
        "voice_clip_gap_sec": VOICE_CLIP_GAP_SEC,
        "total_voice_gap_sec": round(total_voice_gap_sec, 3),
        "background_image": str(background_image),
        "has_intro_video": intro_item is not None,
        "intro_video_path": str(intro_item.media_path) if intro_item is not None else "",
        "intro_duration_sec": round(intro_item.duration_sec, 3) if intro_item is not None else 0.0,
        "subtitle_segments": len(subtitle_segments),
        "display_video_segments": len(display_video_segments),
        "price_transition_title_segments": len(price_transition_title_segments),
        "missing_subtitle_texts": missing_subtitle_texts,
        "image_fallback": match_summary.get("image_fallback", {}),
        "skipped_entries": match_summary.get("skipped_entries", []),
        "skipped_display_videos": match_summary.get("skipped_display_videos", []),
        "unmatched_audio": match_summary.get("unmatched_audio", []),
        "unused_images": match_summary.get("unused_images", []),
    }


def main() -> None:
    args = parse_args()
    try:
        ensure_runtime_dependencies()
        if args.intro_video and args.template_draft:
            raise SkillError("不能同时传入前置引言视频和模板草稿目录，请二选一。")
        draft_root = Path(args.draft_root).resolve()
        draft_root.mkdir(parents=True, exist_ok=True)
        background_image = Path(args.background_image).resolve()
        if not background_image.exists():
            raise FileNotFoundError(f"背景图不存在：{background_image}")
        template_draft_dir = Path(args.template_draft).resolve() if args.template_draft else None
        template_content: dict[str, Any] | None = None
        if template_draft_dir is not None:
            template_content, _ = load_template_draft(template_draft_dir)

        if args.manifest:
            manifest_path = Path(args.manifest).resolve()
            if not manifest_path.exists():
                raise FileNotFoundError(f"Manifest 不存在：{manifest_path}")
            image_index_path = (
                Path(args.image_index_path).resolve()
                if args.image_index_path
                else DEFAULT_IMAGE_INDEX_PATH.resolve()
            )
            items, match_summary = build_items_from_manifest(
                manifest_path,
                image_category=normalize_text(args.image_category),
                image_user=normalize_text(args.image_user),
                image_set=normalize_text(args.image_set),
                image_index_path=image_index_path,
            )
            manifest_mode = match_summary["match_mode"]
        else:
            if not args.image_dir or not args.audio_dir:
                raise SkillError("如果不传 manifest，就必须同时传 `--image-dir` 和 `--audio-dir`。")
            items, match_summary = build_items_from_directories(Path(args.image_dir).resolve(), Path(args.audio_dir).resolve())
            manifest_path = None
            manifest_mode = match_summary["match_mode"]

        draft_name = args.draft_name
        if draft_name == DEFAULT_DRAFT_NAME and manifest_path is not None:
            draft_name = build_default_draft_name(manifest_path)
        final_draft_name = draft_name
        final_draft_dir = draft_root / final_draft_name
        staging_draft_name = f"{final_draft_name}.__building__{uuid.uuid4().hex[:8]}"
        intro_audio_dir = draft_root / INTRO_AUDIO_CACHE_DIR_NAME / safe_path_component(draft_name)
        intro_item = build_intro_clip_item(Path(args.intro_video).resolve(), intro_audio_dir) if args.intro_video else None

        canvas_width = int(template_content.get("canvas_config", {}).get("width", args.width)) if template_content else args.width
        canvas_height = int(template_content.get("canvas_config", {}).get("height", args.height)) if template_content else args.height
        fps = int(template_content.get("fps", args.fps)) if template_content else args.fps

        if template_draft_dir is None:
            summary = build_generated_draft(
                draft_root=draft_root,
                draft_name=staging_draft_name,
                canvas_width=canvas_width,
                canvas_height=canvas_height,
                fps=fps,
                background_image=background_image,
                items=items,
                intro_item=intro_item,
                manifest_path=manifest_path,
                manifest_mode=manifest_mode,
                match_summary=match_summary,
                subtitle_model=args.subtitle_model,
                subtitle_language=args.subtitle_language,
                subtitle_engine=args.subtitle_engine,
                skip_subtitles=args.skip_subtitles,
                subtitle_vad_filter=not args.subtitle_no_vad,
                allow_replace=False,
            )
            staging_draft_dir = Path(summary["draft_dir"])
            replace_draft_dir_atomically(staging_draft_dir, final_draft_dir, args.allow_replace)
            summary["draft_name"] = final_draft_name
            summary["draft_dir"] = str(final_draft_dir)
            patch_draft_content_media_metadata(final_draft_dir)
            update_draft_meta(final_draft_dir, final_draft_name, float(summary["total_duration_sec"]))
        else:
            with tempfile.TemporaryDirectory(prefix="jy-tail-") as temp_root:
                temp_root_path = Path(temp_root).resolve()
                tail_name = f"{staging_draft_name}__tail"
                tail_summary = build_generated_draft(
                    draft_root=temp_root_path,
                    draft_name=tail_name,
                    canvas_width=canvas_width,
                    canvas_height=canvas_height,
                    fps=fps,
                    background_image=background_image,
                    items=items,
                    intro_item=None,
                    manifest_path=manifest_path,
                    manifest_mode=manifest_mode,
                    match_summary=match_summary,
                    subtitle_model=args.subtitle_model,
                    subtitle_language=args.subtitle_language,
                    subtitle_engine=args.subtitle_engine,
                    skip_subtitles=args.skip_subtitles,
                    subtitle_vad_filter=not args.subtitle_no_vad,
                    allow_replace=True,
                )
                staging_final_dir = draft_root / staging_draft_name
                final_duration_sec, merge_summary = merge_template_with_generated_tail(
                    template_draft_dir,
                    Path(tail_summary["draft_dir"]),
                    staging_final_dir,
                    staging_draft_name,
                    False,
                    draft_root=draft_root,
                    canvas_width=canvas_width,
                    canvas_height=canvas_height,
                    fps=fps,
                )
                replace_draft_dir_atomically(staging_final_dir, final_draft_dir, args.allow_replace)
                patch_draft_content_media_metadata(final_draft_dir)
                update_draft_meta(final_draft_dir, final_draft_name, final_duration_sec)
                summary = {
                    **tail_summary,
                    "draft_root": str(draft_root),
                    "draft_name": final_draft_name,
                    "draft_dir": str(final_draft_dir),
                    "template_draft_dir": str(template_draft_dir),
                    "assembly_mode": "template_append",
                    "total_duration_sec": round(final_duration_sec, 3),
                    "merge_summary": merge_summary,
                }

        summary["status"] = "success"
        print(json.dumps(summary, ensure_ascii=False, indent=2))
    except Exception as exc:
        print(json.dumps({"status": "failed", "error": str(exc)}, ensure_ascii=False, indent=2))
        sys.exit(1)


